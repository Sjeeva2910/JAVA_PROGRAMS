public class Powof2 {
    public static void main(String[] args) {
        int num=16;
        int i=16;
        while(i<num){
            i=i*2;
        }
        if(i==num){
            System.out.println("pow of 2");
        }
        else{
            System.out.println("not pow of 2");
        }
    }
    
}

